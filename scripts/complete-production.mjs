#!/usr/bin/env node
/**
 * One-shot production wiring: set Vercel DATABASE_URL + DIRECT_URL, run migrations, redeploy.
 *
 * Usage (replace YOUR_DB_PASSWORD with Supabase → Settings → Database → password):
 *   node scripts/complete-production.mjs --password=YOUR_DB_PASSWORD
 *
 * Optional:
 *   --region=us-east-1
 *   --service-role=YOUR_SUPABASE_SERVICE_ROLE_KEY
 *   --bucket=eshu-assets
 */
import { spawnSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { randomBytes } from 'node:crypto';

const PROJECT_REF = 'qyylmniugajchuynvjgv';
const ROOT = resolve(import.meta.dirname, '..');
const SERVER = resolve(ROOT, 'server');

const args = Object.fromEntries(
  process.argv.slice(2).map((a) => {
    const [k, v] = a.replace(/^--/, '').split('=');
    return [k, v ?? 'true'];
  }),
);

const password = args.password;
const region = args.region || 'eu-central-1';
const serviceRole = args['service-role'] || args.serviceRole;
const bucket = args.bucket || 'eshu-assets';

if (!password || password === 'true') {
  console.error('\nMissing --password=YOUR_SUPABASE_DATABASE_PASSWORD\n');
  console.error('Supabase → Project → Settings → Database → Database password\n');
  process.exit(1);
}

if (!serviceRole || serviceRole === 'true') {
  console.error('\nMissing --service-role=YOUR_SUPABASE_SERVICE_ROLE_KEY\n');
  console.error('Supabase → Project Settings → API Keys → service_role (server-only)\n');
  process.exit(1);
}

const encoded = encodeURIComponent(password);
const direct = `postgresql://postgres:${encoded}@db.${PROJECT_REF}.supabase.co:5432/postgres?sslmode=require`;
const pooled = `postgresql://postgres.${PROJECT_REF}:${encoded}@aws-0-${region}.pooler.supabase.com:5432/postgres?sslmode=require`;

const prodEnv = resolve(SERVER, '.env.production');
const sessionSecret = randomBytes(48).toString('hex');

writeFileSync(
  prodEnv,
  `# Generated by scripts/complete-production.mjs — do not commit
NODE_ENV=production
PORT=3000
LOG_LEVEL=info
DATABASE_URL=${pooled}
DIRECT_URL=${direct}
SESSION_SECRET=${sessionSecret}
SESSION_COOKIE_NAME=eshu.sid
SESSION_MAX_AGE_MS=2592000000
SUPABASE_URL=https://${PROJECT_REF}.supabase.co
SUPABASE_ANON_KEY=sb_publishable_ef87uC-eCGwhqArQnb2USQ_3Q6RODkb
SUPABASE_SERVICE_ROLE_KEY=${serviceRole}
CORS_ORIGIN=https://eshu-0-1-4.vercel.app
STORAGE_DRIVER=supabase
STORAGE_SUPABASE_BUCKET=${bucket}
STORAGE_MAX_BYTES=26214400
`,
);

console.log('Wrote server/.env.production (gitignored)');

const run = (cmd, cargs, opts = {}) => {
  const r = spawnSync(cmd, cargs, { stdio: 'inherit', shell: true, cwd: opts.cwd ?? ROOT, env: { ...process.env, ...opts.env } });
  if (r.status !== 0) process.exit(r.status ?? 1);
};

// Test DB + migrate
run('npm', ['run', 'db:deploy'], {
  cwd: SERVER,
  env: { DATABASE_URL: pooled, DIRECT_URL: direct },
});

// Push all vars to Vercel production
const vars = {
  NODE_ENV: 'production',
  LOG_LEVEL: 'info',
  DATABASE_URL: pooled,
  DIRECT_URL: direct,
  SESSION_SECRET: sessionSecret,
  SESSION_COOKIE_NAME: 'eshu.sid',
  SESSION_MAX_AGE_MS: '2592000000',
  SUPABASE_URL: `https://${PROJECT_REF}.supabase.co`,
  SUPABASE_ANON_KEY: 'sb_publishable_ef87uC-eCGwhqArQnb2USQ_3Q6RODkb',
  SUPABASE_SERVICE_ROLE_KEY: serviceRole,
  CORS_ORIGIN: 'https://eshu-0-1-4.vercel.app',
  STORAGE_DRIVER: 'supabase',
  STORAGE_SUPABASE_BUCKET: bucket,
  STORAGE_MAX_BYTES: '26214400',
};

for (const [name, value] of Object.entries(vars)) {
  const r = spawnSync('npx', ['vercel', 'env', 'add', name, 'production', '--force'], {
    input: value,
    stdio: ['pipe', 'inherit', 'inherit'],
    shell: true,
    cwd: ROOT,
  });
  if (r.status !== 0) {
    console.error(`Failed to set Vercel env ${name}`);
    process.exit(1);
  }
  console.log(`Vercel production: ${name}`);
}

console.log('\nDeploying to Vercel production…');
run('npx', ['vercel', 'deploy', '--prod'], { cwd: ROOT });

console.log('\nDone. Test:');
console.log('  https://eshu-0-1-4.vercel.app/healthz');
console.log('  https://eshu-0-1-4.vercel.app/api/auth/supabase/config');

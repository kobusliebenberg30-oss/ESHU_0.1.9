#!/usr/bin/env node
/**
 * Wire a Supabase project into ESHU .env files (gitignored).
 *
 * Usage:
 *   node scripts/wire-supabase-project.mjs --ref=vmnaybsaigsepckfxqsy --password=YOUR_DB_PASSWORD --publishable=sb_publishable_... --service-role=sb_secret_...
 *
 * Optional:
 *   --region=eu-central-1   (pooler host; copy from Supabase Connect → Transaction pooler if unsure)
 *   --cors=https://eshu-0-2-1.vercel.app
 *   --bucket=eshu-assets
 */
import { randomBytes } from 'node:crypto';
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve } from 'node:path';

const ROOT = resolve(import.meta.dirname, '..');
const SERVER = resolve(ROOT, 'server');

const args = Object.fromEntries(
  process.argv.slice(2).map((a) => {
    const [k, v] = a.replace(/^--/, '').split('=');
    return [k, v ?? 'true'];
  }),
);

const ref = args.ref;
const password = args.password;
const publishable = args.publishable;
const serviceRole = args['service-role'] || args.serviceRole;
const poolerPrefix = args.prefix || 'aws-1';
const region = args.region || 'eu-central-1';
const cors = args.cors || 'https://eshu-0-2-1.vercel.app,http://localhost:8080';
const bucket = args.bucket || 'eshu-assets';

if (!ref || !password || password === 'true') {
  console.error('Usage: node scripts/wire-supabase-project.mjs --ref=PROJECT_REF --password=DB_PASSWORD [--publishable=sb_...]');
  process.exit(1);
}

const enc = encodeURIComponent(password);
const direct = `postgresql://postgres:${enc}@db.${ref}.supabase.co:5432/postgres?sslmode=require`;
const pooled = `postgresql://postgres.${ref}:${enc}@${poolerPrefix}-${region}.pooler.supabase.com:6543/postgres?pgbouncer=true&sslmode=require`;
const directPooler = `postgresql://postgres.${ref}:${enc}@${poolerPrefix}-${region}.pooler.supabase.com:5432/postgres?sslmode=require`;
const sessionSecret = randomBytes(48).toString('hex');
const supabaseUrl = `https://${ref}.supabase.co`;

function block(vars) {
  return Object.entries(vars)
    .map(([k, v]) => `${k}=${v}`)
    .join('\n');
}

const common = {
  DATABASE_URL: pooled,
  DIRECT_URL: directPooler,
  SUPABASE_URL: supabaseUrl,
  ...(publishable && publishable !== 'true' ? { SUPABASE_ANON_KEY: publishable } : {}),
  ...(serviceRole && serviceRole !== 'true' ? { SUPABASE_SERVICE_ROLE_KEY: serviceRole } : {}),
  SESSION_SECRET: sessionSecret,
  SESSION_COOKIE_NAME: 'eshu.sid',
  SESSION_MAX_AGE_MS: '2592000000',
  SESSION_COOKIE_SAME_SITE: 'lax',
  CORS_ORIGIN: cors,
  LOG_LEVEL: 'info',
  STORAGE_DRIVER: serviceRole && serviceRole !== 'true' ? 'supabase' : 'local',
  STORAGE_SUPABASE_BUCKET: bucket,
  STORAGE_MAX_BYTES: '26214400',
};

const rootEnv = resolve(ROOT, '.env');
const serverEnv = resolve(SERVER, '.env');

const rootBody = `# Generated by scripts/wire-supabase-project.mjs — do not commit
${block({ ...common, NODE_ENV: 'development', PORT: '3000', STORAGE_LOCAL_DIR: './storage/assets' })}
`;

const serverBody = `# Generated by scripts/wire-supabase-project.mjs — do not commit
${block({ ...common, NODE_ENV: 'development', PORT: '3000', STORAGE_LOCAL_DIR: './storage/assets', CORS_ORIGIN: `${cors},http://localhost:3000` })}
`;

for (const [path, body] of [[rootEnv, rootBody], [serverEnv, serverBody]]) {
  if (existsSync(path)) {
    console.warn(`Skipping ${path} (already exists). Delete or edit manually, or remove file and re-run.`);
  } else {
    writeFileSync(path, body, 'utf8');
    console.log('Wrote', path);
  }
}

console.log('\nNext:');
console.log('  1. In Supabase Connect modal, confirm pooler region matches --region (default eu-central-1).');
console.log('  2. cd server && npm run db:deploy');
console.log('  3. In Supabase Storage, create a private bucket named:', bucket);
console.log('  4. Add the same vars to Vercel → Settings → Environment Variables (Production + Preview).');
console.log('  5. Redeploy eshu-0-2-1 on Vercel.');
console.log('\nESHU env names (not Next.js): SUPABASE_URL + SUPABASE_ANON_KEY (publishable key is OK).');
